public class DoWhile {
    public static void main(String[] args) {
        int count_do = 5;
        do {
            System.out.println("Count is: " + count_do);
            count_do++;
        } while (count_do < 5);
        int count=5;
        while (count<5){
            System.out.println(count);
        }

    }
}
